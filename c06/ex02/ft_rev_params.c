#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	aux;

	while (argc - 1 > 0)
	{
		aux = 0;
		while (argv[argc - 1][aux] != '\0')
		{
			write (1, &argv[argc - 1][aux], 1);
			aux++;
		}
		write (1, "\n", 1);
		argc--;
	}
}
