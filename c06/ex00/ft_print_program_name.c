#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	aux;

	i = argc;
	aux = 0;
	while (argv[0][aux] != '\0')
	{
		write (1, &argv[0][aux], 1);
		aux++;
	}
	write (1, "\n", 1);
}
