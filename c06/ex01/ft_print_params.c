#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	aux;
	int	i;

	i = 1;
	while (i < argc)
	{
		aux = 0;
		while (argv[i][aux] != '\0')
		{
			write (1, &argv[i][aux], 1);
			aux++;
		}
		write (1, "\n", 1);
		i++;
	}
}
